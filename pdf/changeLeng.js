function toggleLanguage(language) {
    if (language == "th") {
        localStorage.setItem("lang", "th");
        document.getElementsByClassName("prefixOpMr").value = "นาย";
        document.getElementsByClassName("prefixOpMs").value = "นางสาว";
        document.getElementsByClassName("prefixOpMrs").value = "นาง";

        document.getElementsByClassName("prefixOpProf").value = "ศ.";
        document.getElementsByClassName("prefixOpAssoc").value = "รศ.";
        document.getElementsByClassName("prefixOpAsst").value = "ผศ.";
    } else if (language == "en") {
        localStorage.setItem("lang", "en");
        document.getElementsByClassName("prefixOpMr").value = "Mr.";
        document.getElementsByClassName("prefixOpMs").value = "Ms.";
        document.getElementsByClassName("prefixOpMrs").value = "Mrs.";

        document.getElementsByClassName("prefixOpProf").value = "Prof.";
        document.getElementsByClassName("prefixOpAssoc").value = "Assoc. Prof.";
        document.getElementsByClassName("prefixOpAsst").value = "Asst. Prof.";
    }
    renderlang();
    return "now language: " + localStorage.lang;
}
